public class Arithmetic {

    public static void main(String[] agrs) {
        int x = 679;
        int y = 575;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        System.out.println(++x);
        System.out.println(--x);
    }
}

